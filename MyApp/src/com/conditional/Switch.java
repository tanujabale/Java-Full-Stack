package com.conditional;

public class Switch {
	public static void main(String[] args) {
		int a=1;
		switch(a) {
		case 1:
			System.out.println("Inside case 1");
			break;
		case 2:
			System.out.println("Inside case 2");
			break;
		default:
			System.out.println("Invalid");
		}
	}

}
